             $('#search-vin').submit(function (event) {
                 //var data = new FormData(this);
         
                 event.preventDefault();
                 $.ajax({
                     type: "POST",
                     url: "{{ route('vins.search') }}?marca=1",
                     data: {
                         "_token": "{{ csrf_token() }}",
                         "vines": $('#vines').val(),
                         "rut": $('#rut').val(),
                     },
                     success: function (data) {
                         $('#preaccordion').show();
                         $('#accordion').collapse('dispose');
                         $('#error-data').html('');
                         $('#error-data').hide();
                         $('#accordion').html(data);
                         $('#accordion').show();
                         $('#accordion').collapse();
                         $('#button-data').show();
                     },
                     error: function (XMLHttpRequest, textStatus, errorThrown) {
         
                         var mensaje = 'Se presento un error inesperado por favor intente mas tarde, o recargue su navegador';
                         if (XMLHttpRequest.status == 400) {
                             if (XMLHttpRequest.responseText != null && XMLHttpRequest.responseText != '') {
                                 $('#error-data').html(XMLHttpRequest.responseText);
                             } else {
                                 $('#error-data').html(campanasNoAsociadaVin);
                             }
                         } else {
                             $('#error-data').html(mensaje);
                         }
         
                         $('#error-data').show();
                         $('#accordion').html('');
                         $('#accordion').hide();
                         $('#button-data').hide();
                         $('#preaccordion').hide();
                     }
                 });
             });
             $("#BtnCollapse").click(function () {
                 $("#collapsePolitics").toggle("slow");
             });
         
         });
      </script>
